namespace DiscoSdk.Models.Enums;

/// <summary>
/// Represents the layout type for forum channels.
/// </summary>
public enum ForumLayoutType
{
	/// <summary>
	/// No default layout has been set for forum channels.
	/// </summary>
	NotSet = 0,

	/// <summary>
	/// Display posts as a list.
	/// </summary>
	ListView = 1,

	/// <summary>
	/// Display posts as a collection of tiles.
	/// </summary>
	GalleryView = 2
}

