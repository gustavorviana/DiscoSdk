namespace DiscoSdk.Models.Enums;

/// <summary>
/// Represents the layout view for forum channels.
/// </summary>
public enum ForumLayout
{
	/// <summary>
	/// Default view layout.
	/// </summary>
	DefaultView = 0,

	/// <summary>
	/// List view layout.
	/// </summary>
	ListView = 1,

	/// <summary>
	/// Gallery view layout.
	/// </summary>
	GalleryView = 2,

	/// <summary>
	/// Unknown layout (for future Discord updates).
	/// </summary>
	Unknown = -1
}

